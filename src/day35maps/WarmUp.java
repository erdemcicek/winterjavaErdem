package day35maps;

// Create a method to print integers from 0 to 100 without using any integer
public class WarmUp {
	
	public static void main(String[] args) {
		withoutInt();		
	}
	public static void withoutInt() {
		
		Character c1 = 'e'-'e';
		Character c2 = 'e';
		
		for ( Character i = c1 ; i < c2 ; i++) {
			System.out.println((int)i);
		}
		
		
		
	}

}
