package day24inheritanceoverriding;

public class Audi extends Car {
	
	public void price() {
		System.out.println("Audi is expensive");
	}

	// overriden class' method
	
	
	// Polymorphism = Method Overloading + Method Overriding
	// that means both "method overloading" and "method overriding" are
	// part of Polymorphism
	
	// Method Overloading (Static Polymorphism) is Compile Time Polymorphism
	// Method Overriding (Dynamic Polymorphism) is Run Time Polymorphism

}
